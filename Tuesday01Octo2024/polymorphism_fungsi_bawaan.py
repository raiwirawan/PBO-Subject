# mendefiniskan x sebagai string 'Hello World!'
x = "Hello World!"

# membuat sebuah tuple (immutable) dengan tiga string
mytuple = ("apple", "banana", "cherry")

# membuat sebuah list (mutable) dengan tiga string
mylist = ["McLaren", "Ferrari", "Pagani"]

# membuat sebuah dictionary atau set (mutable) dengan 3 key-value pairs
thisdict = {"brand": "Ford", "model": "Mustang", "year": 1964}



# mencetak panjang dari karakter string variabel x karakter 'Hello World!'
print(len(x))       # Output: 13

# mencetak panjang dari karakter tuple variabel mytuple yang berisi 3 string
print(len(mytuple)) # Output: 3

# mencetak panjang dari karakter list variabel mylist yang berisi 3 string
print(len(mylist)) # Output: 3

# mencetak panjang dari karakter list variabel thisdict yang berisi 3 key-value pairs string
print(len(thisdict)) # Output: 3